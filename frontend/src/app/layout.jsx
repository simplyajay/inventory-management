import "@/globals.css";
import Providers from "./providers";
import ProtectedLayout from "@/components/Layout/ProtectedLayout";
import Navbar from "@/components/Navbar/Navbar";
import { cookies } from "next/headers";
import { verify } from "jsonwebtoken";

export const metadata = {
  title: "Point of Sale System",
  description: "Generated by create next app",
};

const RootLayout = ({ children }) => {
  const cookieStore = cookies();
  const token = cookieStore.get(process.env.NEXT_PUBLIC_TOKEN);

  let user = null;

  if (token) {
    try {
      user = verify(token.value, process.env.NEXT_PUBLIC_JWT);
    } catch (error) {
      console.error("Error", error);
    }
  }
  return (
    <Providers>
      <html lang="en">
        <body className="antialiased w-screen h-screen m-0 p-0 flex flex-col">
          <Navbar />
          <main className="flex flex-1 ">
            {user ? (
              <ProtectedLayout user={user}> {children}</ProtectedLayout>
            ) : (
              <>{children}</>
            )}
          </main>
          <footer></footer>
        </body>
      </html>
    </Providers>
  );
};

export default RootLayout;
